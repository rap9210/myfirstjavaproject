public class DeclaringVariables {
    public static void main(String[] argv){
        int Test_Score = 94;
        double Banana_Price = 0.50;
        String Test_Taker = "Ron Swanson";

        System.out.println(Test_Taker);
        System.out.println("SCORE: "+ Test_Score);
        System.out.println("In other unrelated news, " +
                "the price of a banana is $"+ Banana_Price);

    }
}
